    private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        EnrollmentDAO dao = new EnrollmentDAO();
        for (Object[] row : dao.getStudentEnrollmentsWithIds())
        {
            Object[] displayRow = { row[2], row[3], row[4] }; 
            model.addRow(displayRow);
        }
    }
    private void loadComboBoxData()
    {
        StudentDAO sDao = new StudentDAO();
        List<Student> students = sDao.getAllStudents();
        jComboBox1.removeAllItems();
        jComboBox3.removeAllItems();
        jComboBox3.addItem(null);
        for (Student s : sDao.getAllStudents())
        {
            jComboBox1.addItem(s);
            jComboBox3.addItem(s);
        }

        CourseDAO cDao = new CourseDAO();
        jComboBox2.removeAllItems();
        for (Course c : cDao.getAllCourses())
        {
            jComboBox2.addItem(c);
        }
    }
