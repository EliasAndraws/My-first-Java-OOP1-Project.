    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    populateTable();

                
    Student s = (Student) jComboBox1.getSelectedItem();
    Course c = (Course) jComboBox2.getSelectedItem();
    String grade = jTextField3.getText();

    if (s != null && c != null)
    {
        EnrollmentDAO dao = new EnrollmentDAO();
        dao.enrollStudent(s.getStudentId(), c.getCourseId(), grade);
        
        JOptionPane.showMessageDialog(this, "Enrollment added for: " + s.getFirstName());
        populateTable();
        
        jTextField3.setText("");
    }

    try (java.io.FileWriter fw = new java.io.FileWriter("out.txt", true))
    {
    fw.write("New Enrollment: Student [" + s.getFirstName() + " " + s.getLastName() + "] enrolled in course [" + c.getCourseName() + "] with grade: [" + grade + "] on Date: " + new java.util.Date() + "\n");
    }
    catch (java.io.IOException e)
    {
        e.printStackTrace();
    }// TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1)
    {
        JOptionPane.showMessageDialog(this, "Select an enrollment to delete");
        return;
    }
    
    EnrollmentDAO dao = new EnrollmentDAO();
    List<Object[]> data = dao.getStudentEnrollmentsWithIds();
    
    Object[] selectedData = data.get(selectedRow);
    int sId = (int) selectedData[0];
    int cId = (int) selectedData[1];
    String studentName = (String) selectedData[2];
    String courseName = (String) selectedData[3];

    dao.deleteEnrollment(sId, cId);
    
    JOptionPane.showMessageDialog(this, "Enrollment Deleted");
    populateTable();

    try (java.io.FileWriter fw = new java.io.FileWriter("out.txt", true))
    {
        fw.write("Deleted Enrollment: Student [" + studentName + "] was removed from course [" + courseName + "] on Date: " + new java.util.Date() + "\n");
    }
    catch (java.io.IOException e)
    {
    e.printStackTrace();
    }// TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    int selectedRow = jTable1.getSelectedRow();
    String newGrade = jTextField3.getText();

    if (selectedRow == -1 || newGrade.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Select row and enter new grade");
        return;
    }

    EnrollmentDAO dao = new EnrollmentDAO();
    List<Object[]> data = dao.getStudentEnrollmentsWithIds();
    Object[] selectedData = data.get(selectedRow);

    dao.updateGrade((int)selectedData[0], (int)selectedData[1] , newGrade);
    
    JOptionPane.showMessageDialog(this, "Grade Updated");
    populateTable();// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox3ActionPerformed
    Object selectedItem = jComboBox3.getSelectedItem();
    
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
    jTable1.setRowSorter(sorter);

    if (selectedItem != null)
    {
        String studentName = selectedItem.toString();
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + studentName, 0));
    }
    else
    {
        sorter.setRowFilter(null);
    }// TODO add your handling code here:
    }//GEN-LAST:event_jComboBox3ActionPerformed

